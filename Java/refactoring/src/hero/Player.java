package hero;

public class Player {

    private String playerName;
    private int playerHealth;
    private int playerStrength;
    private int playerMagic;
    private int playerCraftingSkill;

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public int getPlayerHealth() {
        return playerHealth;
    }

    public void setPlayerHealth(int playerHealth) {
        this.playerHealth = playerHealth;
    }

    public int getPlayerStrength() {
        return playerStrength;
    }

    public void setPlayerStrength(int playerStrength) {
        this.playerStrength = playerStrength;
    }

    public int getPlayerMagic() {
        return playerMagic;
    }

    public void setPlayerMagic(int playerMagic) {
        this.playerMagic = playerMagic;
    }

    public int getPlayerCraftingSkill() {
        return playerCraftingSkill;
    }

    public void setPlayerCraftingSkill(int playerCraftingSkill) {
        this.playerCraftingSkill = playerCraftingSkill;
    }
}
